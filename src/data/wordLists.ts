export function findWordInLists(word: string): { source: WordListSource; entry: SeedWord } | null {
  const lower = word.toLowerCase().trim();
  for (const source of Object.keys(WORD_LISTS) as WordListSource[]) {
    const found = WORD_LISTS[source].words.find(w => w.word.toLowerCase() === lower);
    if (found) return { source, entry: found };
  }
  return null;
}
